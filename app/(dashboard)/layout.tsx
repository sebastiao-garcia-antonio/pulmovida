export default function DashboardLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const [sidebarOpen, setSidebarOpen] = useState(false);
  const pathname = usePathname();

  const isMedico = pathname.startsWith("/medico");
  const isPaciente = pathname.startsWith("/paciente");
  const links = isMedico ? medicoLinks : isPaciente ? pacienteLinks : adminLinks;

  useEffect(() => {
    setSidebarOpen(false);
  }, [pathname]);

  useEffect(() => {
    if (sidebarOpen) {
      document.body.style.overflow = "hidden";
    } else {
      document.body.style.overflow = "";
    }
    return () => {
      document.body.style.overflow = "";
    };
  }, [sidebarOpen]);

  return (
    <SessionProvider>
      <div className="flex h-screen bg-[#EFF6FF] font-sans overflow-hidden">
      {/* Overlay mobile */}
      {sidebarOpen && (
        <div
          className="fixed inset-0 bg-black/40 z-40 md:hidden animate-fade-in"
          onClick={() => setSidebarOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`fixed md:static inset-y-0 left-0 z-50 w-64 h-dvh max-h-dvh bg-[#3B82F6] text-white flex flex-col transform transition-transform duration-300 ease-in-out ${
          sidebarOpen ? "translate-x-0" : "-translate-x-full md:translate-x-0"
        }`}
      >
        <div className="h-20 flex items-center px-6 bg-[#2563EB] border-b border-white/20">
          <div className="w-8 h-8 flex-shrink-0 bg-white/20 rounded-lg flex items-center justify-center mr-3">
            <LungsIcon className="w-5 h-5 text-white" strokeWidth={1.5} />
          </div>
          <span className="font-bold text-white tracking-wide truncate">Pulmo Vida</span>
        </div>

        <nav className="flex-1 min-h-0 overflow-y-auto py-4 px-3 space-y-1 pb-24">
          {links.map((link) => {
            const isActive = pathname === link.href;
            return (
              <Link
                key={link.href}
                href={link.href}
                className={`group flex items-center px-3 py-2.5 text-sm font-medium rounded-md transition-colors ${
                  isActive
                    ? "bg-white/25 text-white"
                    : "hover:bg-white/20 text-white/90 hover:text-white"
                }`}
              >
                <svg className="mr-3 flex-shrink-0 h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" aria-hidden="true">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d={link.icon} />
                </svg>
                {link.label}
              </Link>
            );
          })}
        </nav>

        <div className="shrink-0 px-3 py-4 border-t border-white/20 bg-[#2563EB]/30">
          <button
            onClick={() => signOut({ callbackUrl: '/login' })}
            className="w-full flex items-center px-3 py-2.5 text-sm font-medium rounded-md hover:bg-white/20 text-red-100 hover:text-white transition-colors text-left"
          >
            <svg className="mr-3 flex-shrink-0 h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M17 16l4-4m0 0l-4-4m8 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
            Sair
          </button>
        </div>
      </aside>

      {/* Main Content Area */}
      <div className="flex-1 flex flex-col min-w-0 overflow-hidden">
        {/* Mobile Header */}
        <header className="md:hidden bg-white border-b border-[#BFDBFE] h-16 flex items-center justify-between px-4">
          <button
            onClick={() => setSidebarOpen(true)}
            className="p-2 rounded-lg hover:bg-[#EFF6FF] transition-colors"
          >
            <svg className="w-6 h-6 text-[#16201E]" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
            </svg>
          </button>
          <span className="font-bold text-[#16201E] tracking-wide">Pulmo Vida</span>
          <div className="w-10" />
        </header>

        <main className="flex-1 overflow-y-auto bg-[#EFF6FF]">
          <div className="animate-fade-in relative">
            {children}
          </div>
        </main>
      </div>
    </div>
    </SessionProvider>
  );
}
